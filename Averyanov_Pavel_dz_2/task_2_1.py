#1. Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

print('тип результата выражения 15 * 3:', type(15 * 3)) #<class 'int'>
print('тип результата выражения 15 / 3:', type(15 / 3)) #<class 'float'>
print('тип результата выражения 15 // 2:', type(15 // 2)) #<class 'int'>
print('тип результата выражения 15 ** 2:', type(15 ** 2)) #<class 'int'>